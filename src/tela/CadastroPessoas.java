    public void preencher_pessoas() {
        
        if (opcaoAtivo.isSelected()) {
            status = 1;
        }
        if (opcaoInativo.isSelected()) {
            status = 0;
        }
        
        pessoas.setPes_nome(cadastroNome.getText().trim());
        pessoas.setPes_rg(cadastroRG.getText().trim());
        pessoas.setPes_cpf(cadastroCPF.getText().trim());
        pessoas.setPes_tel(cadastroTelefone.getText().trim());
        pessoas.setPes_end(cadastroEndereço.getText().trim());
        pessoas.setPes_status(status);

    }

    public void limparTela() {
        cadastroNome.setText("");
        cadastroRG.setText("");
        cadastroCPF.setText("");
        cadastroTelefone.setText("");
        cadastroEndereço.setText("");

    }

    public void consultaPessoas(int cod) throws SQLException {

        limparTela();
        if (cod < 1) { // INPLEMENTAR: se letra bloquer se não vai dar erro 
            JOptionPane.showMessageDialog(null, cod + " não é um código valido!");
        }

        conexao conexao = new conexao();
        conexao.getConexao();
        String selectSQL = "SELECT * FROM PESSOAS A WHERE A.PES_ID = " + cod;

        Statement pstmtCon;
        pstmtCon = conexao.getConexao().createStatement();

        ResultSet rs = pstmtCon.executeQuery(selectSQL);

        while (rs.next()) {

            //if(rs.getInt("pes_id") == null) // INPLEMENTAR: pular se numero pesquisado não existir
            System.out.println("numero: " + rs.getInt("pes_id"));

            cadastroNome.setText(rs.getString("pes_nome"));
            cadastroRG.setText(rs.getString("pes_rg"));
            cadastroCPF.setText(rs.getString("pes_cpf"));
            cadastroEndereço.setText(rs.getString("pes_end"));
            cadastroTelefone.setText(rs.getString("pes_tel"));
            cadastroData.setDate(rs.getDate("pes_dtcadastro"));

            int status = rs.getInt("pes_status");
            if (status == 1) {
                opcaoAtivo.setSelected(true);
            }
            if (status == 0) {
                opcaoInativo.setSelected(true);
            }
            if (status < 0 || status > 1) {
                JOptionPane.showMessageDialog(null, "Tem algo de errado com o status desse cliente! \n"
                        + "Favor alterar!");
            }
        }

        rs.close();
        pstmtCon.close();

    }

