    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed

        int cod;

        preencher_pessoas();

        try {

            pessoas.setPes_dtcadastro(cadastroData.getDate());
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, "Erro" + error.getMessage());
        }

        try {
            pespes = new IncluirEditarDAO();
            if (acao == 1) {
                cod = pespes.IncluirPessoas(pessoas);
                cadastroCod.setText(String.valueOf(cod));
                JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
                salvarPessoa();
            }
        } catch (SQLException | HeadlessException error) {
            JOptionPane.showMessageDialog(this, "Operação não realizada" + error.getMessage());
        }
        botaoSalvar.setEnabled(false);

        // TODO add your handling code here:
    }//GEN-LAST:event_botaoSalvarActionPerformed

